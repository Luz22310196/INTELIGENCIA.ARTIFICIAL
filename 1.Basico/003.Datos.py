# TIPOS DE DATOS BÁSICOS EN PYTHON

# ENTERO (INT)
numero_entero = 10  # SE ASIGNA UN VALOR ENTERO A LA VARIABLE 'numero_entero'

# FLOTANTE (FLOAT)
numero_flotante = 3.14  # SE ASIGNA UN VALOR FLOTANTE A LA VARIABLE 'numero_flotante'

# CADENA DE TEXTO (STR)
nombre = "Carlos"  # SE ASIGNA UNA CADENA DE TEXTO A LA VARIABLE 'nombre'

# BOOLEANO (BOOL)
activo = True  # SE ASIGNA UN VALOR BOOLEANO (True) A LA VARIABLE 'activo'
inactivo = False  # SE ASIGNA UN VALOR BOOLEANO (False) A LA VARIABLE 'inactivo'

# LISTA (LIST)
colores = ["rojo", "verde", "azul"]  # SE ASIGNA UNA LISTA A LA VARIABLE 'colores'

# TUPLA (TUPLE)
coordenadas = (10, 20)  # SE ASIGNA UNA TUPLA A LA VARIABLE 'coordenadas'

# CONJUNTO (SET)
frutas = {"manzana", "banana", "naranja"}  # SE ASIGNA UN CONJUNTO A LA VARIABLE 'frutas'

# DICCIONARIO (DICT)
persona = {"nombre": "Ana", "edad": 30}  # SE ASIGNA UN DICCIONARIO A LA VARIABLE 'persona'

# IMPRESIÓN DE LOS TIPOS DE DATOS

print(type(numero_entero))  # IMPRIME EL TIPO DE 'numero_entero', DEBE MOSTRAR <class 'int'>
print(type(numero_flotante))  # IMPRIME EL TIPO DE 'numero_flotante', DEBE MOSTRAR <class 'float'>
print(type(nombre))  # IMPRIME EL TIPO DE 'nombre', DEBE MOSTRAR <class 'str'>
print(type(activo))  # IMPRIME EL TIPO DE 'activo', DEBE MOSTRAR <class 'bool'>
print(type(colores))  # IMPRIME EL TIPO DE 'colores', DEBE MOSTRAR <class 'list'>
print(type(coordenadas))  # IMPRIME EL TIPO DE 'coordenadas', DEBE MOSTRAR <class 'tuple'>
print(type(frutas))  # IMPRIME EL TIPO DE 'frutas', DEBE MOSTRAR <class 'set'>
print(type(persona))  # IMPRIME EL TIPO DE 'persona', DEBE MOSTRAR <class 'dict'>
