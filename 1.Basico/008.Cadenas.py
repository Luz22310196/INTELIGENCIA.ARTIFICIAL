# FRAGMENTOS DE CADENAS: CONCATENACIÓN Y OPERACIONES BÁSICAS

# DEFINIMOS DOS FRAGMENTOS DE CADENAS
fragmento1 = "La programación es como la vida:"  # FRAGMENTO 1
fragmento2 = "llena de errores, pero también de posibilidades."  # FRAGMENTO 2

# CONCATENAMOS LOS DOS FRAGMENTOS DE CADENAS
frase_completa = fragmento1 + fragmento2  # SE UNE EL FRAGMENTO 1 Y 2

# IMPRIMIMOS LA CADENA COMPLETA
print(frase_completa)  # SE MUESTRA LA CADENA UNIDA

# SEGUNDO EJEMPLO DE CONCATENACIÓN CON UN ESPACIO ADICIONAL
frase_completa = fragmento1 + " " + fragmento2  # AÑADIMOS UN ESPACIO ENTRE LOS FRAGMENTOS

# IMPRIMIMOS LA CADENA CON ESPACIO ADICIONAL
print(frase_completa)  # SE MUESTRA LA CADENA CON ESPACIO

