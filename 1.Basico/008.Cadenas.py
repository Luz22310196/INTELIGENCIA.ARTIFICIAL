# PARTE 2: OPERACIONES CON VARIABLES

# DEFINIMOS DOS VARIABLES NUMÉRICAS
a = 10
b = 30

# REALIZAMOS UNA SUMA Y LA INCLUIMOS EN UN F-STRING
resultado = f"La suma de {a} + {b} es: {a + b}"  # INCLUIMOS EL RESULTADO DE LA SUMA EN UNA CADENA

# IMPRIMIMOS EL RESULTADO DE LA SUMA
print(resultado)  # SE MUESTRA EL RESULTADO DE LA SUMA

# PARTE 3: MODIFICACIONES DE TEXTO

# DEFINIMOS UNA FRASE EN MINÚSCULAS
frase = "la imaginación es el principio de la creación."  # FRASE EN MINÚSCULAS

# CONVERTIMOS LA PRIMERA LETRA A MAYÚSCULA
frase_convertida = frase.capitalize()  # UTILIZAMOS 'capitalize()' PARA HACER MAYÚSCULA LA PRIMERA LETRA

# IMPRIMIMOS LA FRASE CON LA PRIMERA LETRA MAYÚSCULA
print(frase_convertida)  # SE MUESTRA LA FRASE CON LA PRIMERA LETRA EN MAYÚSCULA

# OBTENEMOS EL NOMBRE DEL USUARIO
nombre = input("Introduzca su nombre:\n")  # SOLICITAMOS EL NOMBRE AL USUARIO

# CONVERTIMOS LA PRIMERA LETRA DEL NOMBRE A MAYÚSCULA
nombre_convertido = nombre.capitalize()  # UTILIZAMOS 'capitalize()' PARA HACER MAYÚSCULA LA PRIMERA LETRA DEL NOMBRE

# IMPRIMIMOS UN MENSAJE CON EL NOMBRE CONVERTIDO
print(f"Hola {nombre_convertido}.")  # SE MUESTRA EL NOMBRE CON LA PRIMERA LETRA EN MAYÚSCULA

# DEFINIMOS UNA FRASE CON LETRAS EN MAYÚSCULA Y MINÚSCULA
