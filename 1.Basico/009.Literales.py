# AQUÍ ESTOY USANDO NÚMEROS ENTEROS

entero_positivo = 150  # NÚMERO ENTERO POSITIVO
entero_negativo = -42  # NÚMERO ENTERO NEGATIVO
entero_grande = 1000000  # NÚMERO ENTERO GRANDE

# IMPRIMIR LITERALES ENTEROS
print("Literal entero positivo:", entero_positivo)  # MUESTRA EL NÚMERO ENTERO POSITIVO
print("Literal entero negativo:", entero_negativo)  # MUESTRA EL NÚMERO ENTERO NEGATIVO
print("Literal entero grande:", entero_grande)  # MUESTRA EL NÚMERO ENTERO GRANDE

# PARTE 2: LITERALES FLOTANTES
# EN ESTA PARTE PRUEBO CON NÚMEROS FLOTANTES

flotante_positivo = 3.14159  # NÚMERO FLOTANTE POSITIVO
flotante_negativo = -0.007  # NÚMERO FLOTANTE NEGATIVO
flotante_cientifico = 1e3  # NOTACIÓN CIENTÍFICA: 1 * 10^3 = 1000.0

# IMPRIMIR LITERALES FLOTANTES
print("Literal flotante positivo:", flotante_positivo)  # MUESTRA EL NÚMERO FLOTANTE POSITIVO
print("Literal flotante negativo:", flotante_negativo)  # MUESTRA EL NÚMERO FLOTANTE NEGATIVO
print("Literal flotante en notación científica:", flotante_cientifico)  # MUESTRA EL NÚMERO EN NOTACIÓN CIENTÍFICA

# PARTE 3: LITERALES DE CADENAS
# AQUÍ ESTOY TRABAJANDO CON CADENAS DE TEXTO

cadena_simple = "Hola, ¿cómo estás?"  # CADENA SIMPLE DE TEXTO
cadena_con_comillas = "Él dijo: 'El mapache es hermoso'."  # CADENA CON COMILLAS DENTRO
cadena_espacios = "  Espacios antes y después  "  # CADENA CON ESPACIOS AL PRINCIPIO Y AL FINAL

# IMPRIMIR LITERALES DE CADENAS
print("Cadena simple:", cadena_simple)  # MUESTRA LA CADENA SIMPLE
print("Cadena con comillas dentro:", cadena_con_comillas)  # MUESTRA LA CADENA CON COMILLAS
print("Cadena con espacios:", cadena_espacios)  # MUESTRA LA CADENA CON ESPACIOS

# PARTE 4: OPERACIONES CON LITERALES
# AHORA HAGO ALGUNAS OPERACIONES CON LOS LITERALES NUMÉRICOS

# OPERACIÓN CON ENTEROS
suma_enteros = entero_positivo + entero_negativo  # SUMA DE LOS NÚMEROS ENTEROS
multiplicacion_enteros = entero_grande * 2  # MULTIPLICACIÓN DE UN ENTERO GRANDE POR 2

# OPERACIÓN CON FLOTANTES
suma_flotantes = flotante_positivo + flotante_negativo  # SUMA DE LOS NÚMEROS FLOTANTES
producto_flotantes = flotante_cientifico * 0.5  # PRODUCTO DE UN FLOTANTE POR 0.5

# IMPRIMIR RESULTADOS DE OPERACIONES
print("Suma de enteros:", suma_enteros)  # MUESTRA EL RESULTADO DE LA SUMA DE ENTEROS
print("Multiplicación de enteros:", multiplicacion_enteros)  # MUESTRA EL RESULTADO DE LA MULTIPLICACIÓN DE ENTEROS
print("Suma de flotantes:", suma_flotantes)  # MUESTRA EL RESULTADO DE LA SUMA DE FLOTANTES
print("Producto de flotantes:", producto_flotantes)  # MUESTRA EL RESULTADO DEL PRODUCTO DE FLOTANTES

# PARTE 5: CONVERSIÓN DE LITERALES
# AQUÍ PRUEBO LA CONVERSIÓN ENTRE TIPOS DE DATOS

# CONVERTIR ENTERO A FLOTANTE
entero_a_flotante = float(entero_positivo)  # CONVERTIMOS UN ENTERO A FLOTANTE

# CONVERTIR FLOTANTE A ENTERO
flotante_a_entero = int(flotante_positivo)  # CONVERTIMOS UN FLOTANTE A ENTERO

# IMPRIMIR CONVERSIONES
print("Entero convertido a flotante:", entero_a_flotante)  # MUESTRA EL ENTERO CONVERTIDO A FLOTANTE
print("Flotante convertido a entero:", flotante_a_entero)  # MUESTRA EL FLOTANTE CONVERTIDO A ENTERO

