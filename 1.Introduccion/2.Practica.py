# TEXTO CON UN SOLO ARGUMENTO
print("No me repruebe profe")

# TEXTO CON VARIOS ARGUMENTOS Y ESPACIOS PREDETERMINADOS
print("Ojala", "Luz", "Pase", "sus", "materias")

# EL SEP NOS AYUDA A PERSONALIZAR LA SEPARACIÓN ENTRE PALABRAS
print("Ojala", "Luz", "Pase", "sus", "materias", sep="-")

# SE PERSONALIZA EL FIN DE LA LÍNEA DE TEXTO
print("Ojala", "Luz", "Pase", "sus", "materias", end="!")

# IMPRIMIR DOS MENSAJES EN LA MISMA LÍNEA
print("Ojala", "Luz", "Pase", "sus", "materias", end=" ")
print("y", "que", "no", "tenga", "ningún", "problema.")

